package Date1105;

public class While02 {
    public static void main(String[] args) {

        int sum = 0;
        int i = 1;
        int endNum=100;

        while (i<=endNum){


            sum = sum+i;
            System.out.println("i="+ i + "sum=" + sum);
            i = i+1;

        }
    }

}
